package org.icepdf.core.pobjects.structure.exceptions;

/**
 *
 */
public class CrossReferenceStateException extends Exception {

    public CrossReferenceStateException() {
    }

    public CrossReferenceStateException(String message) {
        super(message);
    }

    public CrossReferenceStateException(String message, Throwable cause) {
        super(message, cause);
    }

    public CrossReferenceStateException(Throwable cause) {
        super(cause);
    }
}
